//35.题目：一个5位数，判断它是不是回文数。即12321是回文数，个位与万位相同，十位与千位相同
#include<stdio.h>
main()
{
	int i,j,k,t,m,n;
	printf("请输入一个五位数\n");
	scanf("%d",&i);
	j=i/10000;
	k=(i-10000*j)/1000;
	t=(i-10000*j-1000*k)/100;
	m=(i-10000*j-1000*k-100*t)/10;
	n=(i-10000*j-1000*k-100*t-10*m);
	if(n==j && k==m)
		printf("这是一个回文数");
	else
		printf("这不是一个回文数"); 
 } 
